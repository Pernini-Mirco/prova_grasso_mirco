CREATE DATABASE IF NOT EXISTS analisi_nba;
USE analisi_nba;

DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS games;
DROP TABLE IF EXISTS players;
DROP TABLE IF EXISTS team_stats;
DROP TABLE IF EXISTS teams;

CREATE TABLE teams (
  id INT PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  abbreviation VARCHAR(10) NOT NULL,
  conference VARCHAR(10) NOT NULL,
  division VARCHAR(30) NOT NULL,
  city VARCHAR(50) NOT NULL
);
CREATE TABLE team_stats (
  team_id INT PRIMARY KEY,
  wins INT NOT NULL,
  losses INT NOT NULL,
  win_pct DECIMAL(5,3) NOT NULL,
  points_per_game DECIMAL(5,1) NOT NULL,
  points_allowed_per_game DECIMAL(5,1) NOT NULL,
  rebounds_per_game DECIMAL(5,1) NOT NULL,
  assists_per_game DECIMAL(5,1) NOT NULL,
  recent_form DECIMAL(4,2) NOT NULL,
  FOREIGN KEY (team_id) REFERENCES teams(id)
);
CREATE TABLE players (
  id INT PRIMARY KEY,
  team_id INT NOT NULL,
  name VARCHAR(100) NOT NULL,
  position VARCHAR(5) NOT NULL,
  age INT NOT NULL,
  points_per_game DECIMAL(5,1) NOT NULL,
  rebounds_per_game DECIMAL(5,1) NOT NULL,
  assists_per_game DECIMAL(5,1) NOT NULL,
  efficiency DECIMAL(5,1) NOT NULL,
  FOREIGN KEY (team_id) REFERENCES teams(id)
);
CREATE TABLE games (
  id INT PRIMARY KEY,
  game_date DATE NOT NULL,
  home_team_id INT NOT NULL,
  away_team_id INT NOT NULL,
  home_score INT NOT NULL,
  away_score INT NOT NULL,
  status VARCHAR(20) NOT NULL,
  arena VARCHAR(100) NOT NULL,
  FOREIGN KEY (home_team_id) REFERENCES teams(id),
  FOREIGN KEY (away_team_id) REFERENCES teams(id)
);
CREATE TABLE users (
  email VARCHAR(120) PRIMARY KEY,
  password VARCHAR(120) NOT NULL,
  role VARCHAR(20) NOT NULL,
  name VARCHAR(100) NOT NULL
);

